char	*ft_strlowcase(char *str);

char	*ft_strlowcase(char *str)
{
	char	*start;

	*start = str;
	while (*str)
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str + 32;
		}
		str++;
	}
	return (start);
}
